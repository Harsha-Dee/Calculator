package com.example.calculator

class Helper {

    companion object {

        fun add(left: Double, right: Double): Double {
            return left + right
        }

        fun subtract(left: Double, right: Double): Double {
            return left - right
        }

        fun divide(left: Double, right: Double): Double {
            return left / right
        }

        fun multiply(left: Double, right: Double): Double {
            return left * right
        }
    }

}